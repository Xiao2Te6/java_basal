public class ComparisonOperatorDemo6
{
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.println("a > b为" + (a > b));
        System.out.println("a < b为" + (a < b));
        System.out.println("a = b为" + (a == b));
        System.out.println("a >= b为" + (a >= b));
        System.out.println("a <= b为" + (a <= b));
        System.out.println("a != b为" + (a != b));

    }
}
