public class VariableDemo1
{
    public static void main(String[] args)
    {
        //byte类型
        byte byteNum = 1;
        System.out.println("bytrNum:" + byteNum);

        //short类型
        short shortNum = 200;
        System.out.println("shortNum:" + shortNum);

        //int类型
        int intNum = 65536;
        System.out.println("intNum:" + intNum);

        //long类型
        long longNum = 13153545134312L;
        System.out.println("longNum:" + longNum);

        //float类型
        float floatNum = 2.33F;
        System.out.println("floatNum:" + floatNum);

        //double类型
        double doubleNum = 12.564;
        System.out.println("doubleNum:" + doubleNum);

        //char类型
        char charSymbol = 'W';
        System.out.println("charSymbol:" + charSymbol);

        //boolean类型
        boolean booleanType = false;
        System.out.println("booleanType:" + booleanType);

        //String 类型
        String stringType = "hello";
        System.out.println("stringType:" +stringType );

    }
}
